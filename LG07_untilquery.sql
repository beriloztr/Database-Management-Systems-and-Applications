CREATE TABLE STUDENTS_(
STID number(3) not null,
STNAME varchar2(10 )not null,
STSURNAME varchar2(20) not null,
STADDRESS varchar2(15),
CONSTRAINT STUDENTS_PK PRIMARY KEY (STID)
);



CREATE TABLE BOOKS_(
ISBN varchar2(10) not null,
TITLE varchar2(30) not null,
STATUS char(1),
CONSTRAINT BK_PK PRIMARY KEY (ISBN),
CONSTRAINT CH_STAT check (status = 'A' or status = 'N')
);

CREATE TABLE AUTHORS_ (
AID number(3) not null,
ANAME varchar2(10),
ASURNAME varchar2(20),
COUNTRY varchar2(10),
CONSTRAINT ATHS_PK PRIMARY KEY (AID) 
);

CREATE TABLE BOOKAUT_ (
ISBN varchar2(10) not null,
AUID number(3) not null,
CONSTRAINT BKAUT_PK PRIMARY KEY (ISBN, AUID),
CONSTRAINT BKAUT_FK1 FOREIGN KEY (AUID) REFERENCES AUTHORS_ (AID),
CONSTRAINT BKAUT_FK2 FOREIGN KEY (ISBN) REFERENCES BOOKS_ (ISBN) );

CREATE TABLE BORROWING_ (
STUID number(3) not null,
BISBN varchar2(10) not null,
BORROWDATE date default sysdate not null,
RETURNDATE date,
DUEDATE date,
PUNISHMENT number(10,2) default 0,
CONSTRAINT BRRW_PK PRIMARY KEY (STUID,BISBN,BORROWDATE),
CONSTRAINT BRRW_FK1 FOREIGN KEY (STUID) REFERENCES STUDENTS_ (STID),
CONSTRAINT BRRW_FK2 FOREIGN KEY (BISBN) REFERENCES BOOKS_ (ISBN) );

